VERSION_LOCAL="1.0.2"

REPO_SCRIPT="https://raw.githubusercontent.com/NeTenebraes/neYTMusic-Downloader/main/neYTMusic.sh"
CHANGELOG_URL="https://raw.githubusercontent.com/NeTenebraes/neYTMusic-Downloader/main/CHANGELOG.md"

CONFIGDIR="$HOME/.config/neYTMusic"
MODULESDIR="$CONFIGDIR/Modules"
PROXY_FILE="$CONFIGDIR/proxy"
LIST_FILE="$CONFIGDIR/list"
ARCHIVE="$CONFIGDIR/archive.txt"
PROXY=""
URL=""
DEST="$HOME/Music/YTMusic"

# Carga módulos Bash
if [ -d "$MODULESDIR" ]; then
  for module_script in "$MODULESDIR"/*.sh; do
    [ -e "$module_script" ] && source "$module_script"
  done
fi

echo "Versión actual: $VERSION_LOCAL"

# Ejecutar actualización
UPDATE_CHECK

mkdir -p "$CONFIGDIR"
mkdir -p "$DEST"
cd "$DEST"

# Pregunta principal
read -r -p "¿Quieres descargar música de una Playlist? [Y/n]: " do_dl
case "$do_dl" in
  [Nn]*)
    echo "Saltando descarga. Reproduciendo con mpv en modo aleatorio..."
    shopt -s nullglob
    files=("$DEST"/*.mp3)
    if [ ${#files[@]} -eq 0 ]; then
      echo "No se encontraron archivos mp3 para reproducir. Por favor descarga primero."
      exit 1
    else
      nohup mpv --shuffle "${files[@]}" > /dev/null 2>&1 &
      exit 0
    fi
    ;;
  [Yy]*|"")
    # Selección de playlist
    if [[ -f "$LIST_FILE" ]]; then
      LAST_URL=$(cat "$LIST_FILE")
      LIST_NAME="$(echo "$LAST_URL" | sed 's|.*list=||;s|&.*||')"
      read -r -p "¿Quieres usar la lista anterior (ID: $LIST_NAME)? [Y/n]: " use_last
      case "$use_last" in
        [Yy]*|"")
          URL="$LAST_URL"
          ;;
        [Nn]*)
          URL=""
          ;;
      esac
    fi

    if [[ -z "$URL" ]]; then
      read -r -p "Pega la URL de la nueva playlist de YouTube Music: " NEW_URL
      if [[ -n "$NEW_URL" ]]; then
        URL="$NEW_URL"
        echo "$URL" > "$LIST_FILE"
      fi
    fi

    if [[ ! "$URL" =~ ^https://music\.youtube\.com/playlist\?list= ]]; then
      echo -e "\nERROR: Solo se admiten playlists de YouTube Music (music.youtube.com)."
      echo "La URL ingresada no corresponde a YouTube Music."
      exit 1
    fi

    # Proxy interactivo
    if [[ -f "$PROXY_FILE" ]]; then
      LAST_PROXY=$(cat "$PROXY_FILE")
      read -r -p "¿Quieres usar el proxy anterior ($LAST_PROXY)? [Y/n]: " use_last_proxy
      case "$use_last_proxy" in
        [Yy]*|"")
          PROXY="$LAST_PROXY"
          ;;
        [Nn]*)
          PROXY=""
          ;;
      esac
    fi

    if [[ -z "$PROXY" ]]; then
      read -r -p "¿Quieres usar un proxy para la descarga? [socks5://127.0.0.1:9050 | 192.168.1.1:8228]  [Enter para omitir]: " NEW_PROXY
      if [[ -n "$NEW_PROXY" ]]; then
        PROXY="$NEW_PROXY"
        echo "$PROXY" > "$PROXY_FILE"
      fi
    fi

    if [[ -n "$PROXY" ]]; then
      PROXY_ARG="--proxy $PROXY"
    else
      PROXY_ARG=""
    fi

    # Descargar en segundo plano
    echo "Iniciando descarga en segundo plano..."
    nohup yt-dlp $PROXY_ARG --yes-playlist -x --audio-format mp3 --embed-thumbnail --embed-metadata --download-archive "$ARCHIVE" "$URL" > /dev/null 2>&1 &

    # Reproducir los archivos disponibles
    shopt -s nullglob
    files=("$DEST"/*.mp3)

    if [ ${#files[@]} -eq 0 ]; then
      echo "No se encontraron canciones descargadas aún. Espera mientras se descarga..."
      sleep 10
      files=("$DEST"/*.mp3)
      if [ ${#files[@]} -eq 0 ]; then
        echo "Aún no hay canciones para reproducir. Fin."
        exit 1
      fi
    fi

    echo "Reproduciendo con mpv en modo aleatorio..."
    nohup mpv --shuffle "${files[@]}" > /dev/null 2>&1 &

    # Opcional: esperar procesos o terminar aquí
    wait
    ;;
esac